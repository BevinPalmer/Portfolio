async function buildRetouchingPairs() {
  const dir = path.join(ROOT, "public", "retouching");
  const entries = await safeReaddir(dir);

  const byBase = new Map();

  for (const entry of entries) {
    if (!entry.isFile()) continue;
    const name = entry.name;
    if (name === ".gitkeep") continue;
    const ext = path.extname(name).toLowerCase();
    if (!IMAGE_EXTS.has(ext)) continue;
    const stem = name.slice(0, -ext.length);
    const match = stem.match(/^(.*?)[-_]+(before|after)$/i);
    if (!match) continue;
    const base = match[1];
    const kind = match[2].toLowerCase();
    const key = base.toLowerCase();
    const rec = byBase.get(key) ?? { id: base };
    if (kind === "before") rec.beforeFile = name;
    if (kind === "after") rec.afterFile = name;
    byBase.set(key, rec);
  }

  const rows = Array.from(byBase.values())
    .filter((x) => x.beforeFile && x.afterFile)
    .sort((a, b) => a.id.localeCompare(b.id, undefined, { sensitivity: "base" }));

  const items = [];
  for (const x of rows) {
    const beforePath = path.join(dir, x.beforeFile);
    const dims = await probeDims(beforePath);
    const width = dims?.width ?? 1000;
    const height = dims?.height ?? 1500;
    items.push({
      id: x.id,
      before: `/retouching/${encodeURIComponent(x.beforeFile)}`,
      after: `/retouching/${encodeURIComponent(x.afterFile)}`,
      width,
      height,
    });
  }

  const code = `// This file is auto-generated by scripts/generate-manifests.mjs\nexport const retouchingPairsManifest = ${JSON.stringify(
    items,
    null,
    2,
  )} as const;\n`;
  await writeTS(path.join(ROOT, "lib", "generated", "retouchingPairs.ts"), code);
}

function clientFromSlug(slug) {
  return slug
    .split(/[-_]+/)
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join(" ");
}

async function buildCampaigns() {
  const baseDir = path.join(ROOT, "public", "campaigns");
  const entries = await safeReaddir(baseDir);
  const dirs = entries
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .filter((name) => name !== ".gitkeep")
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

  const items = [];
  for (const slug of dirs) {
    const campaignDir = path.join(baseDir, slug);
    const campaignEntries = await safeReaddir(campaignDir);
    const files = campaignEntries
      .filter((e) => e.isFile())
      .map((e) => e.name)
      .filter((name) => name !== ".gitkeep")
      .filter((name) => IMAGE_EXTS.has(path.extname(name).toLowerCase()));

    const heroFile = files
      .filter((name) => {
        const stem = name.slice(0, -path.extname(name).length).toLowerCase();
        return stem === "hero";
      })
      .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))[0];

    // Flat layout: frame-*.gif|jpg|… in the campaign folder (not reel/)
    const frames = files
      .filter((name) => /^frame[-_]?\d+/i.test(name.slice(0, -path.extname(name).length)))
      .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
      .map((name) => `/campaigns/${encodeURIComponent(slug)}/${encodeURIComponent(name)}`);

    items.push({
      slug,
      client: clientFromSlug(slug),
      hero: heroFile
        ? `/campaigns/${encodeURIComponent(slug)}/${encodeURIComponent(heroFile)}`
        : "",
      frames,
    });
  }

  const code = `// This file is auto-generated by scripts/generate-manifests.mjs\nexport const campaignsManifest = ${JSON.stringify(
    items,
    null,
    2,
  )} as const;\n\nexport type CampaignManifestEntry = (typeof campaignsManifest)[number];\n`;

  await writeTS(path.join(ROOT, "lib", "generated", "campaigns.ts"), code);
}

await Promise.all([
  buildPhotography(),
  buildHero(),
  buildPortfolio(),
  buildRetouchingPairs(),
  buildCampaigns(),
]);

