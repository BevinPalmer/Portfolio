import fs from "node:fs/promises";
import { createReadStream } from "node:fs";
import path from "node:path";
import probe from "probe-image-size";

const ROOT = process.cwd();

const IMAGE_EXTS = new Set([".jpg", ".jpeg", ".png", ".gif", ".webp"]);
const VIDEO_EXTS = new Set([".mp4", ".webm"]);

function titleFromFilename(filename) {
  const base = filename.replace(/\.[^/.]+$/, "");
  const cleaned = base.replace(/[_-]+/g, " ").replace(/\s+/g, " ").trim();
  return cleaned.length ? cleaned : "Untitled";
}

async function safeReaddir(dir) {
  return fs.readdir(dir, { withFileTypes: true }).catch(() => []);
}

async function probeDims(filePath) {
  try {
    const stream = createReadStream(filePath);
    const res = await probe(stream);
    stream.destroy();
    if (!res || typeof res.width !== "number" || typeof res.height !== "number") {
      return null;
    }
    return { width: res.width, height: res.height };
  } catch {
    return null;
  }
}

async function writeTS(outPath, code) {
  await fs.mkdir(path.dirname(outPath), { recursive: true });
  await fs.writeFile(outPath, code, "utf8");
}

async function buildPhotography() {
  const baseDir = path.join(ROOT, "public", "photography");
  const categories = ["fashion", "creative", "ecommerce"];
  const out = {};

  for (const category of categories) {
    const dir = path.join(baseDir, category);
    const entries = await safeReaddir(dir);
    const files = entries
      .filter((e) => e.isFile())
      .map((e) => e.name)
      .filter((name) => name !== ".gitkeep")
      .filter((name) => IMAGE_EXTS.has(path.extname(name).toLowerCase()))
      .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

    const items = [];
    for (const name of files) {
      const stem = name.slice(0, -path.extname(name).length);
      const dims = await probeDims(path.join(dir, name));
      items.push({
        slug: stem,
        src: `/photography/${category}/${encodeURIComponent(name)}`,
        title: titleFromFilename(stem),
        alt: titleFromFilename(stem),
        width: dims?.width,
        height: dims?.height,
      });
    }
    out[category] = items;
  }

  const code = `// This file is auto-generated by scripts/generate-manifests.mjs\nexport const photographyManifest = ${JSON.stringify(
    out,
    null,
    2,
  )} as const;\n`;

  await writeTS(path.join(ROOT, "lib", "generated", "photography.ts"), code);
}

async function buildHero() {
  const dir = path.join(ROOT, "public", "hero");
  const entries = await safeReaddir(dir);
  const files = entries
    .filter((e) => e.isFile())
    .map((e) => e.name)
    .filter((name) => name !== ".gitkeep")
    .filter((name) => {
      const ext = path.extname(name).toLowerCase();
      return IMAGE_EXTS.has(ext) || VIDEO_EXTS.has(ext);
    })
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

  const items = files.map((name) => {
    const title = titleFromFilename(name);
    const ext = path.extname(name).toLowerCase();
    return {
      kind: VIDEO_EXTS.has(ext) ? "video" : "image",
      src: `/hero/${encodeURIComponent(name)}`,
      alt: title,
      title,
    };
  });

  const code = `// This file is auto-generated by scripts/generate-manifests.mjs\nexport const heroManifest = ${JSON.stringify(
    items,
    null,
    2,
  )} as const;\n`;
  await writeTS(path.join(ROOT, "lib", "generated", "hero.ts"), code);
}

